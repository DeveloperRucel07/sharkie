class Keyboard {

    /**
     * Object to track the state of specific keys for game controls.
     * Properties:LEFT, RIGHT, UP, DOWN, SPACE, ENTER, D, F - Boolean values indicating if the key is pressed.
     */
    constructor() {
        this.LEFT = false;
        this.RIGHT = false;
        this.UP = false;
        this.DOWN = false;
        this.SPACE = false;
        this.ENTER = false;
        this.D = false;
        this.F = false;

    }


}
